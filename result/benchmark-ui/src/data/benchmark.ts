export const SIZES = [100, 1000, 10000, 100000] as const

export type AvgRow = {
  size: number
  forUs: number
  stream: number
  parallel: number
}

export function avg(size: number, col: 2 | 3 | 4): number {
  const rows = RAW.filter((r) => r[1] === size)
  return rows.reduce((s, r) => s + r[col], 0) / rows.length
}

export function computeAvgs(): AvgRow[] {
  return SIZES.map((size) => ({
    size,
    forUs: avg(size, 2),
    stream: avg(size, 3),
    parallel: avg(size, 4),
  }))
}

export type Winner = 'for' | 'stream' | 'parallel'

export function pickWinner(row: AvgRow): Winner {
  const m = Math.min(row.forUs, row.stream, row.parallel)
  if (row.forUs <= m + 1e-9) return 'for'
  if (row.stream <= m + 1e-9) return 'stream'
  return 'parallel'
}

